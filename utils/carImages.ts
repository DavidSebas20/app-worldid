// Función para obtener una imagen aleatoria para una marca específica
export function getRandomCarImage(marca: string): string {
  // Convertir la marca a minúsculas para hacer la búsqueda insensible a mayúsculas/minúsculas
  const marcaLower = marca.toLowerCase();

  // Buscar imágenes para la marca específica
  let imageOptions = defaultImages;

  // Buscar coincidencias exactas primero
  if (brandImages[marcaLower]) {
    imageOptions = brandImages[marcaLower];
  } else {
    // Si no hay coincidencia exacta, buscar coincidencias parciales
    for (const [brand, urls] of Object.entries(brandImages)) {
      if (marcaLower.includes(brand) || brand.includes(marcaLower)) {
        imageOptions = urls;
        break;
      }
    }
  }

  // Seleccionar una imagen aleatoria de las opciones disponibles
  const randomIndex = Math.floor(Math.random() * imageOptions.length);
  return imageOptions[randomIndex];
}

// Función para obtener una imagen genérica aleatoria
export function getRandomDefaultImage(): string {
  const randomIndex = Math.floor(Math.random() * defaultImages.length);
  return defaultImages[randomIndex];
}
